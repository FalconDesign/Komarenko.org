<h1>Услуги и цены</h1>

<p>
	Очень важно! Одинаковые услуги на разных сайтах очень сильно отличаются в цене. Причина не в жажде наживы, недооценке или переоценке, а в уровне исполнения. По крайней мере, мы надеемся. Мы имеем возможность представить весь диапазон этих цен.
</p>

<p>Для вас будет исполнять заказ масимально возможный по качеству исполнитель за тот бюджет, которым вы располагаете.</p>

